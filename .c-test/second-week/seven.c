/*打印出三角形九九乘法表
1 2 3 4 5 6 7 8 9
-----------------------------------------------------------
 1
 2 4
 3 6 9
 4 8 12 16
  ...*/
#include <stdio.h>
int main()
{
    int i, j;
    printf("1   2   3   4   5   6   7   8   9\n");
    printf("-----------------------------------------------------\n");
    for (i = 1; i <= 9; i++)
    {
        for (j = 1; j <= i; j++)
        {
            printf("%d  ", i * j);
        }
        printf("\n");
    }
    return 0;
}